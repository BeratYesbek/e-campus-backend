package com.mb.software.ecampus.entities.concretes;

import com.mb.software.ecampus.core.entities.User;
import com.mb.software.ecampus.core.entities.abstracts.DbEntity;
import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.NoArgsConstructor;

import javax.persistence.*;

@Entity
@Data
@NoArgsConstructor
@AllArgsConstructor
@Table(name = "system_administrators")
public class SystemAdministrator extends DbEntity {

    @Id
    @JoinColumn(name = "system_administrator_id")
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private int id;

    @ManyToOne
    @JoinColumn(name = "user_id")
    private User user;

}
